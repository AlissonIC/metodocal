    ];

    public static function nome(?string $uf): ?string
    {
        if (! $uf) return null;
        return self::LISTA[strtoupper($uf)] ?? $uf;
    }

    public static function ufs(): array
    {
        return array_keys(self::LISTA);
    }
}
